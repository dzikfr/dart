void main(){
  
  print(6*6);
  print(8-190);
  print(10/3);
  print(10~/3);
  print(10%3);

  //augmented operator

  var a = 10;
  print(a+=20);

  a = a+10;
  print(a);

  var i = 0;

  print(++i);
  print(i++);
  print(i);
  // pada intnya, jika ++berada didepan, maka angka itu akan ditambah 1
  //sedangkan jika ++ nya terdapat pada di belakang, maka penambahannya akan dilakukan setelah melewatinya
}